def add(lst,v):
    if v and v not in lst: lst.append(v)

def main():
    db=json.load(open(DB,encoding="utf-8"))
    for r in db:
        t=r.get("tags") or {}
        setting=t.get("setting") or ""
        c={k:[] for k in CATS}
        # 장르/배경
        if setting in SET2G: add(c["장르/배경"],SET2G[setting])
        if setting not in ("fantasy_supernatural","historical_palace"):
            add(c["장르/배경"],"현대물")
        # 남자주인공
        for m in (t.get("male_lead") or []): add(c["남자주인공"],ML2M.get(m))
        # 관계·소재·분위기 (trope/story 기반)
        for tp in (t.get("trope_tags") or []):
            add(c["관계"],TR2R.get(tp)); add(c["소재"],TR2S.get(tp)); add(c["분위기"],TR2MOOD.get(tp))
        add(c["분위기"],STORY2MOOD.get(t.get("story_type") or ""))
        # 수작업 병합
        h=HAND.get(r["id"],{})
        for v in h.get("G",[]): add(c["장르/배경"],v)
        for v in h.get("S",[]): add(c["소재"],v)
        for v in h.get("R",[]): add(c["관계"],v)
        for v in h.get("F",[]): add(c["여자주인공"],v)
        for v in h.get("M",[]): add(c["분위기"],v)
        c={k:[x for x in v if x] for k,v in c.items()}
        r["cats"]=c
        r["tag_version"]="v4.0"
    json.dump(db,open(DB,"w",encoding="utf-8"),ensure_ascii=False,indent=1)
    # 통계
    from collections import Counter
    for k in CATS:
        cc=Counter(x for r in db for x in r["cats"][k])
        empty=sum(1 for r in db if not r["cats"][k])
        print(f"[{k}] 빈:{empty}/76 | {dict(cc.most_common())}")

if __name__=="__main__": main()
